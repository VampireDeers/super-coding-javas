package exercise.chapter_13;

public class IntegerRange {

    public static void main(String[] args){
        // Byte 범위: -128 ~ 127
        // Short 범위: -32768 ~ 32767
        int myInt = 5;
        long myLong = 1123213L;
        System.out.println(myInt);
        System.out.println(myLong);
    }
}
